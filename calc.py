def add(x,y):
    #Die Funktion add addiert
    return x + y
    
def subtract(x,y):
    #Die Funktion subtract subtrahiert
    return x-y

def multiply(x,y):
    #Die Funktion multiply multiplizert
    return x*y

def divide(x,y):
    #Die Funtktion divide dividiert
    return x/y
